        <h1>Differential Tuition</h1>

        <h2>Overview</h2>
        <img src="images/corky_lathe.png" alt="student working on a new lathe" />

        <p>Differential tuition, originally approved in 2008, has had a significant impact on improving the Student Shop. In <a href="/about/docs/StudentWants2009Final.pdf">discussion forums with engineering students</a> there has been a consistent interest in both improving shop access and improving training. We have listened to these requests and are working hard to meet students' expectations. In the fall of 2008 the shop implemented a new permit system that incorporated training before the permit was issued (training was not available prior to this and permits had to be re-applied for each year). Then again in the fall of 2009 the shop improved on this training by also requiring students to attend a seminar on the lathe and mill. New seminars were added as well including woodworking, TIG and MIG welding seminars and we are developing more. These include seminars on using a plasma torch, MasterCAM, 2 and 3 axis CNC milling machines, and a CNC lathe.</p>
        <p>Since 2005 (when record keeping began) the shop has had an increase in student use of 132%. The shop is also being used more by researchers and classes. To meet the growing demand the shop has hired more staff to help students working on projects in the shop and increase the number of seminars being offered. In addition to more staff and seminars the shop will also be open more towards the end of the semester (when students use it most) with later evening hours and Saturday hours.We have also implemented an online application and machine reservation system which both significantly simplifies the process of getting a shop permit and allows students to reserve machines online. This system eliminates most of the scheduling problems encountered in the past and allows students to reserve machines up to two weeks in advance, a week more than was possible under the old system.</p>

        <p>The shop is also bringing machines and equipment up to date. An average machine in an educational setting has a life expectancy of 15 years if it is manual and 10 years if it is CNC. Most of the machines in the shop were between 30 and 50 years old prior to 2008. In addition to updating machines the shop has also increased the number of high demand machines including lathes, mills, drill presses and band saws.</p>

        <p>While being able to fabricate your own design on a manual mill or lathe is excellent we are aware that students would like to work with machines they will actually see in industry and while manual lathes and mills are certainly in industry there are far more 3, 4, and 5 axis CNC machining centers. Industry also uses 3-dimensional laser scanners, metal rapid prototyping, exotic material laser machining and abrasive water jet cutting.  The shop is currently working on acquiring modern technologies that are relavent to student projects in the shop and developing training for them.</p>

        <p>Each semester students can expect an improved shop experience and this is in large part due to differential tuition. Choosing to continue differential tuition is choosing to continue improving shop access, training and your education. To learn more about differential tuition see the <a href="https://www.engr.wisc.edu/admissions/undergraduate-admissions/differential-tuition/" target="_blank">Wisconsin Engineering Student Council's page on differential tuition</a>.

